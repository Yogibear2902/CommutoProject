package com.sandeep.Commuto.Model;

public class Location {

    private String LocationName;
    private double LocationXcoordinate;
    private double LocationYcoordinate;
    private double LocationZcoordinate;

    public String getLocationName() {
        return LocationName;
    }

    public void setLocationName(String locationName) {
        LocationName = locationName;
    }

    public double getLocationXcoordinate() {
        return LocationXcoordinate;
    }

    public void setLocationXcoordinate(double locationXcoordinate) {
        LocationXcoordinate = locationXcoordinate;
    }

    public double getLocationYcoordinate() {
        return LocationYcoordinate;
    }

    public void setLocationYcoordinate(double locationYcoordinate) {
        LocationYcoordinate = locationYcoordinate;
    }

    public double getLocationZcoordinate() {
        return LocationZcoordinate;
    }

    public void setLocationZcoordinate(double locationZcoordinate) {
        LocationZcoordinate = locationZcoordinate;
    }
}
